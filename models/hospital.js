const mongoose = require('mongoose')
const schema = mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    phone:{
        type:Number,
        required:true
    },
    address:{
        type:String,
        required:true
    }
}
    ,{collection:"Hospital"},{strict:true})

module.exports = mongoose.model("hospital",schema)